/* Screencast Loops */

console("-----Loops-----")

var b = 10; //Sets up the index

while(b > 0){ //Checks the condition
    console.log(b + "This is cool!");
    b--; //Increments or decrements the index
}

console("-----Do While Loops")

var c = 10;

do{
    console.log(c + "This is cool!");
    c--;
}
while(c > 0);